#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*new;
	int		i;

	len = ft_strlen(s1);
	new = (char *)malloc(sizeof(char) * (len + 1));
	i = 0;
	if (!new)
		return (NULL);
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
